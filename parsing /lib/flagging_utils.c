#include <stdio.h>

int	is_and_op(char *src)
{
	return ((src[0] == '&' && src[1] == '&'));
}

int	is_or_op(char *src)
{
	return ((src[0] == '|' && src[1] == '|'));
}

int	is_pipe_op(char *src)
{
	return ((src[0] == '|'));
}